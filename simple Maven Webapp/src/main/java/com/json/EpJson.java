package com.json;

public class EpJson {
	private Integer id;
	private String name;
	private Integer epState;
	private String epWayString;
	private Integer rapperState;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getEpState() {
		return epState;
	}
	public void setEpState(Integer epState) {
		this.epState = epState;
	}
	public String getEpWayString() {
		return epWayString;
	}
	public void setEpWayString(String epWayString) {
		this.epWayString = epWayString;
	}
	public Integer getRapperState() {
		return rapperState;
	}
	public void setRapperState(Integer rapperState) {
		this.rapperState = rapperState;
	}
	
}
